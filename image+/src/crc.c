uint16_t crc16(uint16_t init_value, void *src_addr, uint32_t byte_count)
{
	uint16_t crc_code = init_value;
    unsigned char *p = (unsigned char*)src_addr;

    while(byte_count--)
        crc_code = (crc_code << 8) ^ awTable_CRC16[(crc_code >> 8) ^ *p++];
    return crc_code;
}

uint32_t crc32(uint32_t init_value, void *src_addr, uint32_t byte_count)
{
    uint32_t crc_code = init_value;
    unsigned char *p = (unsigned char *)src_addr;

    while(byte_count--)
        crc_code = ulTable_CRC32[(crc_code ^ *p++) & 0xFFL] ^ (crc_code >> 8);
    return crc_code ^ 0xFFFFFFFFul; /* XOR the output value */
} 
